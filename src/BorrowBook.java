    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int selectedRow = jTable1.getSelectedRow();

    if (selectedRow != -1) {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        Object[] rowData = new Object[model.getColumnCount()];

        for (int i = 0; i < rowData.length; i++) {
            rowData[i] = model.getValueAt(selectedRow, i);
        }

        String ISBN = rowData[0].toString();
        String title = rowData[1].toString();
        String author = rowData[2].toString();

        Book bookToDelete = new Book(title,author,ISBN);

        DTOobject.bookList.delete(bookToDelete);
        model.removeRow(selectedRow);
        jTable1.setModel(model);
        LocalDate currentDate = LocalDate.now();      
        // Calculate the return date (10 days after the current date)
        LocalDate returnDate = currentDate.plusDays(10);
        
        // Format the dates
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd-MM-yyyy");
        String currentDateStr = currentDate.format(formatter);
        String returnDateStr = returnDate.format(formatter);
        
        // Display the dialog box
        JOptionPane.showMessageDialog(null, "Book borrowed successfully!\n\nCurrent Date: " + currentDateStr + 
                "\nReturn Date: " + returnDateStr +"\n\n When book returned after due will be charged 100rs p/d"); 
    } else {
        // Display error message or perform any additional operations
        JOptionPane.showMessageDialog(this, "No Book selected!");
    }
    }//GEN-LAST:event_jButton1ActionPerformed

